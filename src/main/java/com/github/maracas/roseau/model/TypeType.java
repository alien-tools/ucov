package com.github.maracas.roseau.model;

/**
 * Enumerates the possible types in Java.
 */
public enum TypeType {

    /**
     * Class type
     */
    CLASS,

    /**
     * Interface type
     */
    INTERFACE,

    /**
     * Record type
     */
    RECORD,

    /**
     * Enum type
     */
    ENUM,

    /**
     * Annotation type
     */
    ANNOTATION
}
